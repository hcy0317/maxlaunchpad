const TRANSLATIONS: Record<AppLanguage, typeof zh> = { zh, en };

export function normalizeLanguage(language: AppSettings['language'] | undefined): AppLanguage {
  return language && language in TRANSLATIONS ? language : 'zh';
}

export function getI18n(language: AppSettings['language'] | undefined): typeof zh {
  return TRANSLATIONS[normalizeLanguage(language)];
}
